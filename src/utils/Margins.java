package utils;

/**
 * The {@Margins} is simple structure to store margins
 *
 * @author olegshchepilov
 *
 */
public class Margins implements Cloneable {
    public Margins() {
        this(0, 0, 0, 0);
    }

    public Margins(int left, int top, int right, int bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }

    @Override
    public Object clone() {
        try {
            return super.clone();
        } catch (CloneNotSupportedException e) {
            throw new InternalError(e);
        }
    }

    @Override
    public String toString() {
        return getClass().getName() + "[left=" + left + ",top=" + top + ",right=" + right + ",bottom=" + bottom + "]";
    }

    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getRight() {
        return right;
    }

    public void setRight(int right) {
        this.right = right;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public int getBottom() {
        return bottom;
    }

    public void setBottom(int bottom) {
        this.bottom = bottom;
    }

    private int left = 0;
    private int top = 0;
    private int right = 0;
    private int bottom = 0;
}
